package hu.oe.hoe.adatok;

public class Ability {
    private byte force;
    private byte brain;
    private byte skill;

    public byte getForce() {
        return force;
    }
    
    public void setForce(byte force) {
        this.force = force;
    }

    public byte getBrain() {
        return brain;
    }
    
    public void setBrain(byte brain) {
        this.brain = brain;
    }

    public byte getSkill() {
        return skill;
    }
    
    public void setSkill(byte skill) {
        this.skill = skill;
    }
}
